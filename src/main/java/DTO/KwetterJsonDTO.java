package DTO;


public class KwetterJsonDTO {
    private String content;
    private int userId;

    public KwetterJsonDTO() {
    }

    public KwetterJsonDTO(String content, int userId) {
        this.content = content;
        this.userId = userId;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }
    
    
}
